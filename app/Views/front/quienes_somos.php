            <h5>Los inicios del Polo IT Corrientes</h5>
              <p>El nacimiento del Polo IT Corrientes empezó a tomar forma en el mes de marzo de 2007, cuando un grupo de   empresas tipo mipymes, mediante el impulso del Gobierno de la Provincia de Corrientes y en el marco del Plan Estratágico para la Industria del Software y Servicios, deja de manifiesto una gran vocación de integración y asociatividad.
                 A partir de este compromiso asumido entre las empresas y con al apoyo del gobierno provincial, se empiezan con reuniones de trabajo periódicas y a dar forma a un anhelo; la concreción efectiva de un Polo de características innovadoras en la Provincia.
                 Las empresas correntinas están integradas por jóvenes profesionales del medio que buscan mediante la asociatividad: el crecimiento individual y conjunto, la transferencia de conocimiento, Investigación y desarrollo, la búsqueda de alianzas estratágicas con organismos y Universidades y la mejora continua como los pilares para fortalecer este espacio.
                 Se destaca la actitud proactiva de sus miembros y una gran motivación en el proyecto encarado, siendo los mismos concientes de la coyuntura favorable y las buenas perspectivas de crecimiento del sector SSI en la Argentina.
                 Los integrantes del grupo han decidido asentar la base principal de sus actividades empresarias en Corrientes, el lugar donde nacieron sus empresas y donde han decidido vivir. Desde este lugar desean desarrollar su compromiso empresario con la comunidad.</p>
